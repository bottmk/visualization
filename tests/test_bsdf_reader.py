class TestLoadBsdfReadersPlugin:
    """load_bsdf_readers() プラグイン自動ロードのテスト。"""

    def test_load_registers_lightools_reader(self, tmp_path, monkeypatch):
        """custom_bsdf_readers/ フォルダから LightToolsBsdfReader を登録できる。"""
        import shutil
        import sys

        # custom_bsdf_readers/ を tmp_path にコピーして plugin_dir 指定でロード
        src_dir = Path(__file__).parent.parent / "custom_bsdf_readers"
        dst_dir = tmp_path / "custom_bsdf_readers"
        shutil.copytree(src_dir, dst_dir)

        # sys.path に追加してモジュール解決できるようにする
        monkeypatch.syspath_prepend(str(tmp_path))

        _READER_REGISTRY.clear()
        load_bsdf_readers(dst_dir)
        assert "LightToolsBsdfReader" in _READER_REGISTRY

    def test_nonexistent_plugin_dir_is_noop(self, tmp_path):
        """存在しないフォルダを指定しても例外が出ない。"""
        load_bsdf_readers(tmp_path / "no_such_dir")  # should not raise


class TestReadBsdfFileWithRealSample:
    """sample_inputs/ の実ファイルを使った統合テスト。"""

    SAMPLE = Path(__file__).parent.parent / "sample_inputs" / "BRDF_BTDF_LightTools.bsdf"

    @pytest.mark.skipif(
        not SAMPLE.exists(),
        reason="sample_inputs/BRDF_BTDF_LightTools.bsdf が存在しない",
    )
    def test_real_file_block_count(self):
        from custom_bsdf_readers.lightools_bsdf import LightToolsBsdfReader
        dfs = LightToolsBsdfReader.read(self.SAMPLE)
        assert len(dfs) == 24

    @pytest.mark.skipif(
        not SAMPLE.exists(),
        reason="sample_inputs/BRDF_BTDF_LightTools.bsdf が存在しない",
    )
    def test_real_file_brdf_btdf_split(self):
        from custom_bsdf_readers.lightools_bsdf import LightToolsBsdfReader
        dfs = LightToolsBsdfReader.read(self.SAMPLE)
        df_all = pd.concat(dfs, ignore_index=True)
        brdf_blocks = sum(1 for d in dfs if (d["mode"] == "BRDF").all())
        btdf_blocks = sum(1 for d in dfs if (d["mode"] == "BTDF").all())
        assert brdf_blocks == 12
        assert btdf_blocks == 12

    @pytest.mark.skipif(
        not SAMPLE.exists(),
        reason="sample_inputs/BRDF_BTDF_LightTools.bsdf が存在しない",
    )
    def test_real_file_row_count(self):
        from custom_bsdf_readers.lightools_bsdf import LightToolsBsdfReader
        dfs = LightToolsBsdfReader.read(self.SAMPLE)
        # 91 radial × 361 azimuth = 32851 rows per block × 24 blocks
        total = sum(len(d) for d in dfs)
        assert total == 91 * 361 * 24

    @pytest.mark.skipif(
        not SAMPLE.exists(),
        reason="sample_inputs/BRDF_BTDF_LightTools.bsdf が存在しない",
    )
    def test_real_file_bsdf_mostly_nonnegative(self):
        """実測データは概ね非負。測定ノイズによる微小な負値は許容する。"""
        from custom_bsdf_readers.lightools_bsdf import LightToolsBsdfReader
        dfs = LightToolsBsdfReader.read(self.SAMPLE)
        for df in dfs:
            bsdf = df["bsdf"].values
            # 大部分が非負であることを確認（1% 以内の負値を許容）
            neg_ratio = (bsdf < 0).sum() / len(bsdf)
            assert neg_ratio < 0.01, f"負値の割合が多すぎる: {neg_ratio:.1%}"
            # 極端な負値がないことを確認
            assert bsdf.min() > -1.0

    @pytest.mark.skipif(
        not SAMPLE.exists(),
        reason="sample_inputs/BRDF_BTDF_LightTools.bsdf が存在しない",
    )
    def test_real_file_via_read_bsdf_file(self):
        from custom_bsdf_readers.lightools_bsdf import LightToolsBsdfReader
        register_reader(LightToolsBsdfReader)
        dfs = read_bsdf_file(self.SAMPLE)
        assert len(dfs) == 24

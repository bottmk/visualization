class TestLightToolsBsdfReaderRead:
    """read() の結果検証。"""

    @pytest.fixture()
    def dfs(self, mini_bsdf_file):
        from custom_bsdf_readers.lightools_bsdf import LightToolsBsdfReader
        return LightToolsBsdfReader.read(mini_bsdf_file)

    def test_two_blocks(self, dfs):
        assert len(dfs) == 2

    def test_block_dtype_columns(self, dfs):
        required = {"u", "v", "theta_s_deg", "phi_s_deg", "theta_i_deg",
                    "phi_i_deg", "wavelength_um", "polarization", "mode",
                    "method", "bsdf", "is_measured", "log_rmse"}
        for df in dfs:
            assert required.issubset(df.columns)

    def test_first_block_brdf(self, dfs):
        assert (dfs[0]["mode"] == "BRDF").all()

    def test_second_block_btdf(self, dfs):
        assert (dfs[1]["mode"] == "BTDF").all()

    def test_first_block_aoi(self, dfs):
        assert np.allclose(dfs[0]["theta_i_deg"].values, 0.0)

    def test_second_block_aoi(self, dfs):
        assert np.allclose(dfs[1]["theta_i_deg"].values, 20.0)

    def test_wavelength_um(self, dfs):
        for df in dfs:
            assert np.allclose(df["wavelength_um"].values, 0.532, atol=1e-4)

    def test_row_count(self, dfs):
        # 3 radial × 4 azimuth = 12 rows per block
        for df in dfs:
            assert len(df) == 12

    def test_bsdf_values_first_block(self, dfs):
        # data_matrix[0, 0] = 1.0 (theta_s=0, phi_s=0)
        assert (dfs[0]["bsdf"].values >= 0.0).all()
        assert pytest.approx(dfs[0]["bsdf"].sum(), rel=1e-3) == pytest.approx(78.0)

    def test_bsdf_values_second_block(self, dfs):
        assert pytest.approx(dfs[1]["bsdf"].sum(), rel=1e-3) == pytest.approx(7.8)

    def test_polarization_unpolarized(self, dfs):
        for df in dfs:
            assert (df["polarization"] == "Unpolarized").all()

    def test_method_is_measured(self, dfs):
        for df in dfs:
            assert (df["method"] == "measured").all()

    def test_is_measured_flag(self, dfs):
        for df in dfs:
            assert df["is_measured"].all()

    def test_phi_s_values(self, dfs):
        phi_vals = set(dfs[0]["phi_s_deg"].round(0).astype(int).unique())
        assert phi_vals == {0, 90, 180, 270}

    def test_theta_s_values(self, dfs):
        theta_vals = sorted(dfs[0]["theta_s_deg"].unique())
        assert len(theta_vals) == 3
        assert pytest.approx(theta_vals[0]) == 0.0
        assert pytest.approx(theta_vals[1]) == 45.0
        assert pytest.approx(theta_vals[2]) == 90.0

    def test_uv_from_theta_phi(self, dfs):
        df = dfs[0]
        theta_rad = np.deg2rad(df["theta_s_deg"].values)
        phi_rad   = np.deg2rad(df["phi_s_deg"].values)
        u_expected = np.sin(theta_rad) * np.cos(phi_rad)
        v_expected = np.sin(theta_rad) * np.sin(phi_rad)
        assert np.allclose(df["u"].values, u_expected, atol=1e-4)
        assert np.allclose(df["v"].values, v_expected, atol=1e-4)



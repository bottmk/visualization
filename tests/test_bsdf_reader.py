# ── build_measured_dataframe の is_btdf パラメータ ─────────────────────────────

class TestBuildMeasuredDataframeIsbtdf:
    """build_measured_dataframe の is_btdf 引数の動作確認。"""

    def _make_df(self, theta_i: float, is_btdf=None) -> pd.DataFrame:
        theta_s = np.array([0.0, 30.0, 45.0])
        phi_s   = np.array([0.0, 90.0, 180.0])
        bsdf    = np.array([1.0, 0.5, 0.2], dtype=np.float32)
        kwargs = {}
        if is_btdf is not None:
            kwargs["is_btdf"] = is_btdf
        return build_measured_dataframe(
            theta_s, phi_s, bsdf, theta_i, 0.0, 532.0, "Unpolarized", **kwargs
        )

    def test_none_small_aoi_defaults_to_brdf(self):
        df = self._make_df(theta_i=30.0)  # is_btdf=None
        assert (df["mode"] == "BRDF").all()

    def test_none_large_aoi_defaults_to_btdf(self):
        df = self._make_df(theta_i=91.0)  # is_btdf=None
        assert (df["mode"] == "BTDF").all()

    def test_explicit_true_overrides_small_aoi(self):
        df = self._make_df(theta_i=20.0, is_btdf=True)
        assert (df["mode"] == "BTDF").all()

    def test_explicit_false_overrides_large_aoi(self):
        df = self._make_df(theta_i=91.0, is_btdf=False)
        assert (df["mode"] == "BRDF").all()

    def test_wavelength_converted_to_um(self):
        df = self._make_df(theta_i=0.0)
        assert np.allclose(df["wavelength_um"].values, 0.532, atol=1e-4)

    def test_method_is_measured(self):
        df = self._make_df(theta_i=0.0)
        assert (df["method"] == "measured").all()

    def test_is_measured_flag(self):
        df = self._make_df(theta_i=0.0)
        assert df["is_measured"].all()


# ── BaseBsdfFileReader / レジストリ ───────────────────────────────────────────

class TestRegistry:
    """プラグインレジストリの動作確認。"""

    def test_register_reader_adds_to_registry(self):
        class MockReader(BaseBsdfFileReader):
            @classmethod
            def can_read(cls, path):
                return path.suffix == ".mock"
            @classmethod
            def read(cls, path):
                return []

        register_reader(MockReader)
        assert "MockReader" in _READER_REGISTRY
        assert "MockReader" in list_readers()

    def test_read_bsdf_file_uses_can_read(self, tmp_path):
        class AlwaysReader(BaseBsdfFileReader):
            @classmethod
            def can_read(cls, path):
                return True
            @classmethod
            def read(cls, path):
                return [pd.DataFrame({"x": [1]})]

        register_reader(AlwaysReader)
        dummy = tmp_path / "dummy.txt"
        dummy.write_text("hello")
        result = read_bsdf_file(dummy)
        assert len(result) == 1
        assert "x" in result[0].columns

    def test_read_bsdf_file_file_not_found(self, tmp_path):
        with pytest.raises(FileNotFoundError):
            read_bsdf_file(tmp_path / "no_such_file.bsdf")

    def test_read_bsdf_file_no_reader_raises(self, tmp_path):
        dummy = tmp_path / "dummy.xyz"
        dummy.write_text("data")
        # レジストリが空の状態で呼ぶ
        _READER_REGISTRY.clear()
        with pytest.raises(ValueError, match="対応する BSDF リーダーが見つからない"):
            read_bsdf_file(dummy)

    def test_read_bsdf_file_by_name(self, tmp_path):
        class NamedReader(BaseBsdfFileReader):
            @classmethod
            def can_read(cls, path):
                return False  # 自動選択では使われないが名前指定なら使える
            @classmethod
            def read(cls, path):
                return [pd.DataFrame({"y": [2]})]

        register_reader(NamedReader)
        dummy = tmp_path / "dummy.dat"
        dummy.write_text("x")
        result = read_bsdf_file(dummy, reader_name="NamedReader")
        assert result[0]["y"].iloc[0] == 2

    def test_read_bsdf_file_unknown_reader_name(self, tmp_path):
        dummy = tmp_path / "dummy.dat"
        dummy.write_text("x")
        with pytest.raises(ValueError, match="未知のリーダー"):
            read_bsdf_file(dummy, reader_name="NoSuchReader")


